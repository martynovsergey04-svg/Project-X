const modules = [];
const moduleData = {};

let globalId = 1;

for (let pIdx = 0; pIdx < phaseNames.length; pIdx++) {
  const phaseName = phaseNames[pIdx];
  
  for (let tIdx = 0; tIdx < moduleTopicsTemplate.length; tIdx++) {
    const t = moduleTopicsTemplate[tIdx];
    // Mark higher tier concepts as gold
    const tier = pIdx >= 10 ? 'gold' : 'basic';
    const id = `m${globalId}`;
    
    // Add phase number conceptually to the title for uniqueness
    const title = `${t.t} (Фаза ${pIdx+1})`;

    modules.push({
      id,
      tier,
      phaseName: phaseName,
      title: title,
      subtitle: t.s,
      description: t.c
    });

    // Random chart for every module
    const isUpTrend = Math.random() > 0.5 ? 'up' : 'down';
    let chartData = [];
    if (globalId % 4 !== 0) { // 75% chance to have a chart
      let p = 50000;
      for(let i=0; i<30; i++){
        let o = p;
        let up = false;
        if (isUpTrend === 'up') up = Math.random() > 0.4;
        else if (isUpTrend === 'down') up = Math.random() < 0.4;
        else up = Math.random() > 0.5;
        
        let volatility = Math.random() * 500 + 100;
        let c = o + (up ? volatility : -volatility);
        let h = Math.max(o,c) + Math.random()*200;
        let l = Math.min(o,c) - Math.random()*200;
        chartData.push({ time: i+'', open: o, high: h, low: l, close: c });
        p = c;
      }
    }
    
    // Every module has an image now
    const imageUrl = images[globalId % images.length];

    const theory = generateRichTheory(phaseName, t, 10, globalId);
    
    // Using phase-specific assignments
    const descriptionText = phaseTaskContext[pIdx] || phaseTaskContext[0];
    const botPromptText = practicalPromptsTemplate[pIdx] || practicalPromptsTemplate[0];

    moduleData[id] = {
      theory: theory,
      chartData: chartData.length > 0 ? chartData : undefined,
      imageUrl: imageUrl,
      quiz: generateQuizArray(t),
      practicalTask: {
        title: `🎯 Практическое задание: Фаза ${pIdx + 1} (${t.t})`,
        description: `${descriptionText}\n\nНажмите **"Отправить Анализ ИИ-Куратору"** и скопируйте картинку/результат прямо в чат для проверки.`,
        botPrompt: botPromptText
      }
    };
    
    globalId++;
  }
}

